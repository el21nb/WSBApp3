package com.example.wsbapp3.models;

/**
 * Model class representing a Jacket and its id
 */
public class Jacket {
    private String id;

    public Jacket() {
    }
    public Jacket(String id) {
        this.id = id;
    }
    public String getIdentifier() {

        return id;
    }

    public void setIdentifier(String id) {

        this.id = id;
    }




}
